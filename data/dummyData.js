// Statistics for testing
export const getDummyStats = () => {
    const totalIncome = dummyTransactions
        .filter(t => t.type === 'income')
        .reduce((sum, t) => sum + t.amount, 0);

    const totalExpense = dummyTransactions
        .filter(t => t.type === 'expense')
        .reduce((sum, t) => sum + t.amount, 0);

    const balance = totalIncome - totalExpense;

    const monthlyIncome = {};
    const monthlyExpense = {};

    dummyTransactions.forEach(t => {
        const month = new Date(t.date).toLocaleString('default', { month: 'long', year: 'numeric' });
        if (t.type === 'income') {
            monthlyIncome[month] = (monthlyIncome[month] || 0) + t.amount;
        } else {
            monthlyExpense[month] = (monthlyExpense[month] || 0) + t.amount;
        }
    });

    return {
        totalIncome,
        totalExpense,
        balance,
        monthlyIncome,
        monthlyExpense,
        transactionCount: dummyTransactions.length,
        budgetCount: dummyBudgets.length
    };
};